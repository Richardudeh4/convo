// ─── Helpers ────────────────────────────────────────────────────────────────

const word = (text, romanization, english) => ({ text, romanization, english });

const spanishFull = (text, romanization, words, breakdown) => ({
  text, romanization, words, breakdown,
});

const opt = (id, english, text, romanization, words, breakdown) => ({
  id, english,
  target: spanishFull(text, romanization, words, breakdown),
});

const single = (id, text, romanization, english, words, breakdown) => ({
  id, type: "single_response",
  target: { text, romanization },
  options: [opt(1, english, text, romanization, words, breakdown)],
});

const listen = (id, text, romanization, words, breakdown, choices, correctId) => ({
  id, type: "listening_mc",
  target: spanishFull(text, romanization, words, breakdown),
  options: choices.map((english, i) => ({ id: i + 1, english })),
  correctOptionId: correctId,
});

const mc = (id, text, romanization, options, correctId) => ({
  id, type: "multiple_choice",
  target: { text, romanization },
  options, correctOptionId: correctId,
});

const stubQ = (id, text, romanization, english, words, breakdown) => ({
  id, type: "single_response",
  target: spanishFull(text, romanization, words, breakdown),
  options: [opt(1, english, text, romanization, words, breakdown)],
});

const lesson = (id, title, icon, questions) => ({
  id, title, icon, completionCount: 0, questions,
});

const review = (id, title, questions) => ({
  id, title, icon: "flash", completionCount: 0, questions,
});

